void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef * hadc)
{
	extern uint16_t ADC_ConvertedValue[6];
	extern uint16_t _ADC_ConvertedValue[10];
	extern uint32_t ACT_ADC_ConvertedValue[6];
	extern uint32_t _ACT_ADC_ConvertedValue[10];
	extern uint16_t ADC_AVG[6];
	extern uint16_t _ADC_AVG[10];

	if(hadc == &hadc1)
	{
		uint16_t i;
		for(i = 0; i < 6; i++)
		{
			ACT_ADC_ConvertedValue[i] += ADC_ConvertedValue[i];
		}
		if(DMA_Transfer_Complete_Count >= Current_Cv)
		{
			for(i = 0; i < 6; i++)
			{
				ADC_AVG[i] = ACT_ADC_ConvertedValue[i] / (Current_Cv + 1);
				ACT_ADC_ConvertedValue[i] = 0;
			}

			DMA_Transfer_Complete_Count = 0;
		}

		DMA_Transfer_Complete_Count++;
	}
	else if(hadc == &hadc3)
	{
		uint16_t j;
		for(j = 0; j < 10; j++)
		{
			_ACT_ADC_ConvertedValue[j] += _ADC_ConvertedValue[j];
		}
		if(_DMA_Transfer_Complete_Count >= Current_Cv)
		{
			for(j = 0; j < 10; j++)
			{
				_ADC_AVG[j] = _ACT_ADC_ConvertedValue[j] / (Current_Cv + 1);
				_ACT_ADC_ConvertedValue[j] = 0;
			}

			_DMA_Transfer_Complete_Count = 0;
		}

		_DMA_Transfer_Complete_Count++;
	}
}

